function particle_flame()
	return LoadActor("fire")..{
		InitCommand=cmd(x,SCREEN_CENTER_X;y,SCREEN_CENTER_Y);
		OnCommand=function(self)
			local ran_varient = math.random();
			local ran_x = math.random(0, 854);
			self:diffusealpha(1);
			self:x(ran_x);
			self:y(480);
			self:accelerate(3 * ran_varient);
			self:addy(-128);
			self:diffusealpha(0);
		end;
	}
end;

